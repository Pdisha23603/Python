a = int(input("enter the value of a is: "))
b = int(input("enter the value of b is: "))
print(f"a>b and b>a is: {a>b and b>a}")   #both condition should be true to make whole condition true
print(f"a>b or b>a is: {a>b or b>a}")     #any one condition should be true to make whole condition true
print(f"not(a>b) is: {not(a>b)}")  #if condition is true then it will return false, if condition is false then it will return true