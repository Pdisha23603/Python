# Find simple Interest . SI=(P*T*R)/100

P = int(input("enter the principal amount: "))
T = int(input("enter the time in years: "))
R = int(input("enter the rate of interest: "))
SI=(P*T*R)/100
print(f"simple interest is: {SI}")


