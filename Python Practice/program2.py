# Find maximum number using max function.

num1 = int(input("enter first number: "))
num2 = int(input("enter second number: "))
num3 = int(input("enter third number: "))
num4 = int(input("enter four number: "))
num5 = int(input("enter five number: "))
maximum = max(num1,num2,num3,num4,num5)
print(f"maximum number is: {maximum}")

# output:
# enter first number: 23
# enter second number: 45
# enter third number: 12
# enter four number: 67
# enter five number: 34
# maximum number is: 67

